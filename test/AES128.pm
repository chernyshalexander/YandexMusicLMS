# Round constants for key schedule (10 rounds for AES-128)
my @Rcon = map { $_ << 24 } (0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36);

# T-tables: Te0[a] = SubBytes(a) mixed with MixColumns coefficients for row 0.
# Te1/Te2/Te3 are 8/16/24-bit right-rotations of Te0 (rows 1/2/3).
# Built once at module load time.
my (@Te0, @Te1, @Te2, @Te3);
{
    for my $i (0..255) {
        my $s  = $S[$i];
        my $s2 = (($s << 1) ^ (($s & 0x80) ? 0x1b : 0)) & 0xff; # GF(2^8) * 2
        my $s3 = $s2 ^ $s;                                        # GF(2^8) * 3
        # Te0: row-0 byte contribution through MixColumns [2,1,1,3] (big-endian word)
        $Te0[$i] = (($s2 << 24) | ($s << 16) | ($s << 8) | $s3) & 0xFFFFFFFF;
        # Te1/Te2/Te3: rotate right by 8/16/24 bits
        $Te1[$i] = (($Te0[$i] >> 8)  | (($Te0[$i] & 0x000000ff) << 24)) & 0xFFFFFFFF;
        $Te2[$i] = (($Te0[$i] >> 16) | (($Te0[$i] & 0x0000ffff) << 16)) & 0xFFFFFFFF;
        $Te3[$i] = (($Te0[$i] >> 24) | (($Te0[$i] & 0x00ffffff) <<  8)) & 0xFFFFFFFF;
    }
}

# Expand 16-byte key into 44 round-key words (AES-128 key schedule).
sub _key_expand {
    my ($key) = @_;
    my @w = unpack('N4', $key);            # 4 big-endian 32-bit words
    for my $i (4..43) {
        my $t = $w[$i - 1];
        if ($i % 4 == 0) {
            # RotWord (rotate left 8) + SubWord + Rcon XOR
            $t = (($t << 8) | ($t >> 24)) & 0xFFFFFFFF;
            $t = ($S[($t >> 24) & 0xff] << 24)
               | ($S[($t >> 16) & 0xff] << 16)
               | ($S[($t >>  8) & 0xff] <<  8)
               |  $S[ $t        & 0xff];
            $t ^= $Rcon[$i / 4 - 1];
        }
        $w[$i] = ($w[$i - 4] ^ $t) & 0xFFFFFFFF;
    }
    return @w;
}

sub new {
    my ($class, $key) = @_;
    die "AES128: key must be 16 bytes\n" unless length($key) == 16;
    return bless { rk => [ _key_expand($key) ] }, $class;
}

# Encrypt a single 16-byte block (ECB). Returns 16 bytes.
# State is 4 big-endian 32-bit words = 4 AES columns.
# ShiftRows byte routing (for col j): row0←col j, row1←col (j+1)%4, row2←col (j+2)%4, row3←col (j+3)%4.
sub encrypt {
    my ($self, $block) = @_;
    my $rk = $self->{rk};

    my ($s0, $s1, $s2, $s3) = unpack('N4', $block);

    # AddRoundKey (round 0)
    $s0 = ($s0 ^ $rk->[0]) & 0xFFFFFFFF;
    $s1 = ($s1 ^ $rk->[1]) & 0xFFFFFFFF;
    $s2 = ($s2 ^ $rk->[2]) & 0xFFFFFFFF;
    $s3 = ($s3 ^ $rk->[3]) & 0xFFFFFFFF;

    # Rounds 1..9: SubBytes + ShiftRows + MixColumns + AddRoundKey (via T-tables)
    for my $r (1..9) {
        my $b = $r << 2;   # base index into round key array
        my ($t0, $t1, $t2, $t3);
        $t0 = ($Te0[($s0>>24)     ] ^ $Te1[($s1>>16)&0xff] ^ $Te2[($s2>>8)&0xff] ^ $Te3[$s3&0xff] ^ $rk->[$b  ]) & 0xFFFFFFFF;
        $t1 = ($Te0[($s1>>24)     ] ^ $Te1[($s2>>16)&0xff] ^ $Te2[($s3>>8)&0xff] ^ $Te3[$s0&0xff] ^ $rk->[$b+1]) & 0xFFFFFFFF;
        $t2 = ($Te0[($s2>>24)     ] ^ $Te1[($s3>>16)&0xff] ^ $Te2[($s0>>8)&0xff] ^ $Te3[$s1&0xff] ^ $rk->[$b+2]) & 0xFFFFFFFF;
        $t3 = ($Te0[($s3>>24)     ] ^ $Te1[($s0>>16)&0xff] ^ $Te2[($s1>>8)&0xff] ^ $Te3[$s2&0xff] ^ $rk->[$b+3]) & 0xFFFFFFFF;
        ($s0,$s1,$s2,$s3) = ($t0,$t1,$t2,$t3);
    }

    # Final round: SubBytes + ShiftRows + AddRoundKey (no MixColumns)
    my $t0 = (($S[$s0>>24    ]<<24)|($S[($s1>>16)&0xff]<<16)|($S[($s2>>8)&0xff]<<8)|$S[$s3&0xff]) ^ $rk->[40];
    my $t1 = (($S[$s1>>24    ]<<24)|($S[($s2>>16)&0xff]<<16)|($S[($s3>>8)&0xff]<<8)|$S[$s0&0xff]) ^ $rk->[41];
    my $t2 = (($S[$s2>>24    ]<<24)|($S[($s3>>16)&0xff]<<16)|($S[($s0>>8)&0xff]<<8)|$S[$s1&0xff]) ^ $rk->[42];
    my $t3 = (($S[$s3>>24    ]<<24)|($S[($s0>>16)&0xff]<<16)|($S[($s1>>8)&0xff]<<8)|$S[$s2&0xff]) ^ $rk->[43];

    return pack('N4',
        $t0 & 0xFFFFFFFF,
        $t1 & 0xFFFFFFFF,
        $t2 & 0xFFFFFFFF,
        $t3 & 0xFFFFFFFF,
    );
}

1;
